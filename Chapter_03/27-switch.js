//switch statements

const grade = 'D';

switch(grade){
    case 'A':
        console.log('Case A');
        break;
    case 'B':
        console.log('Case B');
        break;
    case 'C':
        console.log('Case c');
        break;
    case 'D':
        console.log('Case d');
        break;
    default:
        console.log('No grade');
}

